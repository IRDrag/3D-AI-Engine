import cadquery as cq

# =======================================================
# ГЕОМЕТРИЧЕСКИЕ ПАРАМЕТРЫ ПРОФИЛЯ (в мм)
# =======================================================
h_total = 80         # Полная высота плинтуса-стены
w_floor = 16         # Толщина основания у пола
thick_wall = 5.0     # Толщина верхней зубчатой стены
cable_w = 10         # Ширина скрытого кабель-канала
cable_h = 20         # Высота скрытого кабель-канала
sample_length = 120  # Длина тестового образца плинтуса

# Дополнительный габарит для плавного перехода карниза
thick_main = thick_wall + 1.5  

# =======================================================
# ПОСТРОЕНИЕ 2D-КОНТУРА (Вид сбоку на плоскости XZ)
# =======================================================
profile = (
    cq.Workplane("XZ")
    # Задняя стенка и прямоугольный кабель-канал у пола
    .moveTo(0, 0)
    .lineTo(0, 4)
    .lineTo(cable_w, 4)
    .lineTo(cable_w, 4 + cable_h)
    .lineTo(0, 4 + cable_h)
    .lineTo(0, h_total)
    
    # Лоток (ров) под скрытую LED-ленту сзади парапета
    .lineTo(8, h_total)
    .lineTo(8, h_total - 12)
    .lineTo(8 + thick_wall, h_total - 12)
    
    # Макушка стены и декоративный карниз
    .lineTo(8 + thick_wall, h_total)
    .lineTo(8 + thick_wall + 1.5, h_total - 15)
    .lineTo(8 + thick_main, h_total - 18)
    
    # Лицевая плоскость и плавный спуск к полу
    .lineTo(8 + thick_main, 25)
    
    # Наклонный бастион (утолщение крепости у основания)
    .lineTo(w_floor, 12)
    .lineTo(w_floor, 4)
    
    # Нижний прижимной носик к напольному покрытию
    .lineTo(w_floor + 2, 0)
    .lineTo(w_floor - 2, 0)
    .close()
)

# =======================================================
# ЭКСТРУЗИЯ И ЭКСПОРТ
# =======================================================
# Вытягиваем плоский чертеж в 3D-модель по оси Y
plinth_monolith = profile.extrude(sample_length)

# Сохраняем финальный STL файл
cq.exporters.export(plinth_monolith, 'v1_basic_profile.summary.stl')
