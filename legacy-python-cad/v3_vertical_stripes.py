import cadquery as cq

# =======================================================
# ГЕОМЕТРИЧЕСКИЕ ПАРАМЕТРЫ (в мм)
# =======================================================
h_total = 80         # Высота плинтуса-забора
thick_wall = 5.0     # Толщина стены
cable_w = 10         # Место под кабель-канал у стены

# =======================================================
# 1. ПОСТРОЕНИЕ КОНТУРА КРЕПОСТИ НА ПЛОСКОСТИ XY (Вид сверху)
# =======================================================
# Стартуем из левого заднего угла, прижатого к стене
castle_top = cq.Workplane("XY").moveTo(0, 0)

# Проводим заднюю ровную линию плинтуса по всей длине (120 мм)
castle_top = castle_top.lineTo(0, 120)

# Смещаемся вправо на толщину кабель-канала и стенки парапета
castle_top = castle_top.lineTo(cable_w + thick_wall, 120)

# --- ГЕНЕРАЦИЯ ЗИГЗАГА СТЕНЫ ---
# Идем в обратную сторону по оси Y (от 120 до 0 мм) с шагом 15 мм.
# Формируем чередование: выступ зубца (X=15) и впадина бойницы (X=10).
castle_top = (
    castle_top
    .lineTo(15, 105).lineTo(10, 105)  # Прорезь 1
    .lineTo(10, 90).lineTo(15, 90)    # Зубец 1
    .lineTo(15, 75).lineTo(10, 75)    # Прорезь 2
    .lineTo(10, 60).lineTo(15, 60)    # Зубец 2
    .lineTo(15, 45).lineTo(10, 45)    # Прорезь 3
    .lineTo(10, 30).lineTo(15, 30)    # Зубец 3
    .lineTo(15, 15).lineTo(10, 15)    # Прорезь 4
    .lineTo(10, 0)                    # Финал последней прорези
)

# Замыкаем контур обратно в нулевую точку
castle_top = castle_top.lineTo(0, 0).close()

# =======================================================
# 2. ВЕРТИКАЛЬНАЯ ЭКСТРУЗИЯ
# =======================================================
# Проблема: Выдавливание плоского зигзага строго вверх сформировало 
# вертикальные полосы по всей высоте, исключив возможность сделать 
# кабель-канал, скос бастиона и лоток под LED-ленту.
final_fortress = castle_top.extrude(h_total)

# Экспорт модели-забора
cq.exporters.export(final_fortress, 'v3_vertical_stripes.summary.stl')
